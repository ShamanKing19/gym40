 <span style="font-family: Verdana; font-size: 10pt;"><b>СТОИМОСТЬ ПИТАНИЯ</b></span>
</p>
<p style="text-align: justify;">
 <b>платное питание:</b><br>
	 комплексный завтрак – 83 рублей (5 - 11 классы);<br>
	 комплексный обед – 99 рублей (5 - 11 классы).<br>
 <br>
 <b>бесплатное питание:</b><br>
	 92.47 рублей (для обучающихся 1-4 классов),<br>
	 99.64 рубля (для обучающихся 5-11 классов).<br>
 <br>
	 Стоимость питания на одного обучающегося в день включает&nbsp;расходы на услуги по организации питания, в том числе расходы на продукты питания.
</p>
<p style="text-align: justify; border-bottom: 2px solid black;">
 <span style="font-family: Verdana; font-size: 10pt;"><b>ЛЬГОТНОЕ ПИТАНИЕ</b></span>
</p>
<p style="text-align:justify;">
	 В соответствии с <a target="_blank" href="http://docs.cntd.ru/document/460267316">Постановлением Правительства Калининградской области от 25 декабря 2013 года №1002 «Об обеспечении питанием обучающихся за счет средств областного бюджета»</a> основанием для получения бесплатного питания учащимися гимназии является <u>заявление</u> родителей (законных представителей) учащегося.
</p>
<p style="text-align: justify;">
	 Заявление подаётся на имя директора гимназии перед началом учебного года (в августе).
</p>
<p>
	 В заявлении указываются:<br>
 <i class="fa fa-sign-out check" style="color: SteelBlue"></i> данные заявителя: фамилия, имя, отчество, дата рождения, СНИЛС, адрес регистрации и контактный телефон;<br>
 <i class="fa fa-sign-out check" style="color: SteelBlue"></i> данные учащегося: фамилия, имя, отчество, дата рождения, СНИЛС, класс;<br>
 <i class="fa fa-sign-out check" style="color: SteelBlue"></i> категория семьи (полная, неполная, многодетная, одинокая мать), также указывается, с какого времени семья состоит на учёте в комитете социальной поддержки населения и является малоимущей семьёй.<br>
</p>
<p>
 <a href="/parents/organizatsiya-pitaniya/Образец%20заявления%2019-20.docx" target="_blank"> <i class="fa fa-file-word-o fa-lg" aria-hidden="true"></i> Образец заявления</a>
</p>
<p>
	 Родители учащихся с ОВЗ к заявлению прилагают копию заключения ПМПК. <br>
	 Родители детей-инвалидов к заявлению прилагают копию справки Бюро МСЭ.
</p>
<p>
	 Заявления принимаются: <i class="fa fa-calendar" aria-hidden="true"></i> понедельник-пятница <i class="fa fa-clock-o" aria-hidden="true"></i> 9<sup>00 </sup> до 17<sup>00</sup> <br>
 <br>
	 Питание обучающихся с ограниченными возможностями здоровья (ОВЗ), посещающих образовательное учреждение и обучающихся на дому за счёт субсидии из областного бюджета.<br>
	 Двухразовое горячее питание состоит:<br>
	 - в первую смену (завтрак и обед);<br>
	 - во вторую смену (обед и полдник).<br>
	 Учащиеся с ОВЗ, обучающиеся на дому, обеспечиваются сухим пайком.<br>
	 Норматив обеспечения питанием на одного обучающегося с ОВЗ в день составляет:<br>
	 - 184,94 рублей - для 1-4 классов;<br>
	 - 199,28 рублей - для 5-11 классов.
