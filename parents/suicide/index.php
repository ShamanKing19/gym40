 <i class="fa fa-sign-out check" style="color: Red"></i> если загрузить человека работой, то ему некогда думать о самоубийстве.<br>
 <i class="fa fa-sign-out check" style="color: Red"></i> чужая душа - потемки. Предвидеть попытку самоубийства невозможно.<br>
 <i class="fa fa-sign-out check" style="color: Red"></i> существует некий тип людей,"склонных к самоубийству".<br>
 <i class="fa fa-sign-out check" style="color: Red"></i> не существует никаких признаков, которые указывали бы на то, что человек решился на самоубийство. <br>
 <i class="fa fa-sign-out check" style="color: Red"></i> решение о самоубийстве приходит внезапно, без предварительной подготовки. <br>
