<p>
 <i class="fa fa-sign-out check" style="color: Red"></i> самоубийства совершаются в основном психически ненормальными людьми.<br>
 <i class="fa fa-sign-out check" style="color: Red"></i> самоубийства предупредить невозможно. Тот, кто решил покончить с собой, рано или поздно это сделает.<br>
