<p style="text-align:justify;">
	 С сентября 2022 года по инициативе губернатора Калининградской области реализуется губернаторская программа «УМная PROдленка».
</p>
<p style="text-align: justify;">
	 Основные задачи проекта – максимально приблизить школьников к дополнительному образованию, дать возможность всем детям посещать бесплатно те кружки, которые они захотят выбрать, в независимости от их фактической стоимости, помочь ребятам реализовать их интересы и возможности.
</p>
<p style="text-align: justify;">
	 Результатами реализации губернаторской программы «Умная PROдленка» станут позитивная занятость и всестороннее развитие обучающихся начальной школы во внеурочное время с учетом познавательных потребностей и интересов.
</p>
<p style="text-align: justify;">
	 В МАОУ "Гимназия № 40 им. Ю.А. Гагарина" реализуются 6 программ по направлениям:<br>
 <i class="fa fa-check-circle check" aria-hidden="true"></i> Техническая направленность - "Основы компьютерной грамотности".<br>
 <i class="fa fa-check-circle check" aria-hidden="true"></i> Художественная направленность - "Изобразительное искусство", "Вокал".<br>
 <i class="fa fa-check-circle check" aria-hidden="true"></i> Физкультурно-спортивная направленность - "Плавание", "Танцевальная фитнес-аэробика".<br>
 <i class="fa fa-check-circle check" aria-hidden="true"></i> Социально-гуманитарная направленность - "Иностранный язык".
</p>
<p style="text-align: justify;">
	 Программы размещены в <a target="_blank" href="https://klgd.pfdo.ru/app">Навигаторе дополнительного образования Калининградской области</a> в реестре платных программ и отмечены специальным знаком «Губернаторская программа».
