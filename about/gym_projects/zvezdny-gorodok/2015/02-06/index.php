</p>
<p class="" style="text-align: justify;">
	 &nbsp; &nbsp; &nbsp;Следующей группой, о которой мы бы хотели рассказать - технологи. Стоило нам только зайти, как увидели, что каждый из ребят полностью погружён в свою работу. Настолько, что поначалу никто даже не заметил нашего присутствия! Из-за непрерывного рабочего процесса у них не было времени отвечать на наши вопросы, но всё же нам удалось понять, что сегодня рукодельницы занимались составлением чертежа…будущей юбки! Интересно, что же у них получится? Очень скоро узнаем!
</p>
