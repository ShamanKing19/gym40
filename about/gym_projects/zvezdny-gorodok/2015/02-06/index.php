</p>
<p align="center">
 <b><i><span style="color: #00b050;"><span style="color: #005824;">Группа историков</span> </span></i></b>
</p>
<p class="" style="text-align: justify;">
	 &nbsp; &nbsp; &nbsp;Увлечённые историей своей страны, эти ребята посвятили своё время работе с музейными экспонатами, расположенными на гимназической выставке. Им предстояло поработать над коллажами, связанными с разными этапами нашей истории. Для этого они разделились на группы, и одна из них работала со Смутным временем. Пообщавшись с ними, мы поняли, что команда действительно заинтересована в своей работе и с большим интересом решает поставленные задачи.
</p>
<p style="text-align: center;" class="">
