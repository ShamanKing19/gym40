</p>
<p>
</p>
<p style="text-align: justify;">
	 &nbsp; &nbsp; &nbsp; &nbsp; За небольшой период времени (порядка 2 часов) команды должны были предложить проект эффективного финансирования космических программ (6-8 классы) и план спасения человечества от метеоритной опасности (9-10 классы). Наиболее убедительными среди 6-8 классов оказались участники команды Марс. Они предложили не просто прибыльную программу космического туризма, но и спроектировали целый учебный модуль на орбите. (И всё на хорошем английском!). Порадовали также команды, оказавшиеся призёрами конкурса в<span style="color: windowtext;">&nbsp;старшей возрастной категории.</span>
</p>
<p style="text-align: justify;">
 <span style="color: windowtext;">&nbsp; &nbsp; &nbsp;Примечательно, что организаторы конкурса позаботились не только о том, чтобы у команд было всё необходимое для работы: отдельное помещение с компьютерами, оргтехникой и Интернет, канцелярские принадлежности, но также участникам был предложен обед и полезная досуговая программа.</span>
</p>
