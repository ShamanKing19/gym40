<p class="" style="text-align: justify;">
	 Каникулы – время действий, проб и проверки своих сил. Это высказывание было подтверждено 31 октября учениками, учителями и гостями гимназии №40 им. Ю. А. Гагарина, которые стали очевидцами не только полнолуния и падения метеорита на звездном небе, но и увлекательного конкурса «Парад Планет-2015». Данный конкурс проходит ежегодно в рамках фестиваля «Наука и образование» при поддержке Министерства образования Калининградской области, БФУ им. И. Канта, языковой школы «Эксперт». В этом году гимназия распахнула двери для 22 команд из 17 учебных заведений Калининградской области.
</p>
<p class="" style="text-align: justify;">
	 Конкурс «Парад Планет» представляет собой командное соревнование в умении находить решение ситуационной задачи по астрономии, изложенной на английском языке. Цель команд – разработать стратегию решения ключевой проблемы и защитить разработанный проект.
</p>
<p class="" style="text-align: justify;">
	 Участники конкурса разработали и представили проекты, ориентированные на решение проблем поиска внеземных цивилизаций и утилизации мусора в космосе, посетили мастер-классы в физико-техническом центре гимназии, творческие практикумы «Звезды на ладони» и «Космический авангардизм», создали своими руками удивительной красоты букеты роз из кленовых листьев и почувствовали себя астрономами, нарисовав картину звездного неба.
</p>
<p style="text-align: justify;">
	 Конкурсные проекты оценивало компетентное жюри, в состав которого входили преподаватели БФУ им. И.Канта, языковой школы «Эксперт», представители книжной компании «Британия», а также учителя школ г. Калининграда.
</p>
<p style="text-align: justify;">
	 Жюри, отметив оригинальность, научную обоснованность решений всех команд-участниц, ярких и талантливых, определило победителей и призёров в двух возрастных категориях:
</p>
<p style="text-align: justify;">
	 6-8 классы<br>
	 1. «Сириус», МБОУ СОШ №1 г.Светлый; <br>
	 2. «Арктур», МАОУ гимназия №2 г.Черняховска; <br>
	 3. «Толиман», МАОУ гимназия №32 г.Калининград.
</p>
<p style="text-align: justify;">
	 9-10 классы <br>
	 1. «Спика», МАОУ СОШ№1 г.Светлогорск; <br>
	 2. «Солнце», МАОУ гимназия №40 г.Калининград; <br>
	 3. «Антарес», МАОУ гимназия №32 г.Калининград.
</p>
<p class="" style="text-align: justify;">
	 Этот день подарил много новых открытий. Участники конкурса, покидая гимназию № 40, говорили о своём стремлении принять участие в «Параде планет» в следующем году.
</p>
