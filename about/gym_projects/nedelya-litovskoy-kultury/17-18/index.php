<p style="text-align: justify;">
	 В рамках методического фестиваля тематических уроков «Россия и Литва: диалог культур» открытые уроки провели Людмила Алексеевна Василевская, Инна Васильевна Курамшина, Наталья Валерьяновна Литвинова, Светлана Сергеевна Плиско, Веста Юрьевна Садовая.
</p>
<p style="text-align: justify;">
	 16-я Неделя литовской культуры в гимназии № 40 завершилась торжественной церемонией закрытия, почетными гостями которой стали председатель Совета родительской общественности гимназии №40 Игорь Михайлович Коваль, Генеральный консул Литовской Республики в Калининграде Олегас Скиндерскис.
</p>
<p style="text-align: justify;">
	 Почетные гости подвели итоги гимназических мероприятий, а также наградили дипломами их победителей и участников.
</p>
