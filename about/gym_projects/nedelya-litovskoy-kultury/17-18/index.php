?><p style="text-align: justify;">
	 С 15 по 22 февраля 2018 года в гимназии в шестнадцатый раз был реализован проект «Дни литовской культуры». Проект проводится с 2002 г. в рамках программы «Литовский язык и культура Литвы в российской школе» при поддержке Представительства МИД России в Калининграде и Генерального консульства Литовской Республики в Калининграде.
</p>
<p style="text-align: justify;">
 <a style="color:#428bca;" href="/novosti/2017-2018/12.02/Программа Дней литовской культуры.pdf" target="_blank"><i class="fa fa-file-pdf-o fa-lg text-pdf" aria-hidden="true" style="color:#f00;"></i> Программа мероприятий</a>
</p>
<p style="text-align: justify;">
	 В этом году проект объединил более 1500 учащихся 50 педагогов гимназии – организаторов и участников интеллектуальных конкурсов, викторин, выставок творческих работ.
</p>
<p style="text-align: center;">
 <a rel="lightbox[roadtrip]" href="/novosti/2017-2018/28.02/14.JPG"><img width="30%" src="/novosti/2017-2018/28.02/14.JPG" align="left" style="margin-right: 10px;"></a>
</p>
<p style="text-align: justify;">
	 Традиционно Дни литовской культуры начались с церемонии открытия, прошедшей 15 февраля 2018 года в ККЦ «Галактика» с участием гимназистов 5-6 лингвистических классов, педагогов гимназии, гостей. На церемонии открытия присутствовали директор гимназии №40 Татьяна Павловна Мишуровская, председатель ассоциации учителей литовского языка Калининградской области Алексас Бартникас, заместитель председателя ассоциации учителей литовского языка Калининградской области, координатор проекта «Звучи, звучи, литовская песня!» Альгирдас Кармилавичус, учитель литовского языка МАОУ г. Калининрада гимназии №32 Виктория Кузнецова.
</p>
<p style="text-align: justify;">
	 В ходе мероприятия на литовском языке звучали стихи Саломеи Нерис в исполнении педагога гимназии Елены Валерьевны Новик, а также песни на литовском языке «Мама» и «Солнце будит нас всех» в исполнении ансамбля студии эстрадного вокала «Звуки музыки» (под руководством Натальи Владимировны Федоренко). С песней «Луна» выступила солистка вокальной студии «Канцона» Наталия Белая, работающая под руководством Натальи Валерьяновны Литвиновой.
</p>
<p style="text-align: center;">
 <a rel="lightbox[roadtrip]" href="/novosti/2017-2018/28.02/13.JPG"><img width="30%" src="/novosti/2017-2018/28.02/13.JPG" align="right" style="margin-left: 10px;"></a>
</p>
<p style="text-align: justify;">
	 Участникам церемонии открытия Дней литовского языка и культуры Литвы было предложено участвовать в конкурсе «Караван впечатлений» на составление лучшего отзыва к анимационному фильму «Янтарный замок», созданному режиссером Александрой Снежко-Блоцкой в 1959 году на киностудии «Союзмультфильм» по мотивам древней литовской легенды о красавице русалке Юрате и молодом рыбаке Каститисе и происхождении янтаря. Анимационный фильм был представлен вниманию юных зрителей.
</p>
<p style="text-align: justify;">
	 Особый интерес вызвало обсуждение анимационного фильма «Юрате и Каститис», созданного участниками детского лагеря «Наследие» под руководством Алексаса Бартникаса. Дискуссия прошла за круглым столом с участием руководителя проекта Алекасас Бартникаса, участника проекта Артемия Гадасюка, учащегося гимназии № 32, и учащихся 8-9 классов гимназии №40.
</p>
<p style="text-align: justify;">
	 На протяжении недели в гимназии в информационно-библиотечном центре «Звездный» работала выставка книг «Литовские писатели детям и о детях», организованная педагогом гимназии Ларисой Владимировной Рубцовой. Были представлены произведения К. Донелайтиса, детская литература на литовском языке, книги об истории Литвы разных периодов: от археологии до современного периода; книги, раскрывающие особенности ландшафта Литвы, Куршской косы, книги-путеводители по городам Литвы, книги о культурном наследии страны. По материалам выставки проводились библиотечные уроки.
</p>
<p style="text-align: justify;">
	 В музейном пространстве гимназии «Музейная набережная» работала выставка «Страницы истории культуры Литвы», подготовленная Светланой Анатольевной Чеховской, Еленой Егоровной Минаковой.
</p>
<p style="text-align: justify;">
	 Учитель изобразительного искусства Лариса Николаевна Позднякова организовала дистанционный российско-литовский творческий проект «Литва глазами детей» (Калининград – Клайпеда), результатом которого стала виртуальная выставка рисунков.
</p>
<p style="text-align: justify;">
	 Организаторы горячего питания ООО «Школа» подготовили меню для проведения Недели литовской кухни в гимназии. Евгения Валентиновна Чертова организовала и провела увлекательную интеллектуальную викторину для педагогов гимназии «Секреты литовской кухни».
</p>
<p style="text-align: justify;">
	 21 февраля состоялось занятие «Школы юного дипломата» в Генеральном консульстве Литовской Республики в Калининграде, которое провел Генеральный консул Литовской Республики в Калининграде Олегас Скиндерскис. Господин консул ответил на интересующие молодежь вопросы, подчеркнув необходимость организации культурно-образовательных проектов, способствующих знакомству молодежи соседних стран с культурой и традициями соседних стран.
</p>
