<p style="text-align: justify;">
	 Так, в заочном конкурсе по английскому языку «Письмо другу по переписке в Литве» победу одержала Кобылина Анастасия, в дистанционном интеллектуальном марафоне «Путешествие из Калининграда в Литву» победительницей стала Степанюк Анна, в конкурсе диктантов одержали победу Прилепин Александр, Красикова Дарья, Экстрим Вероника. Победительницей конкурса «Караван впечатлений» стала Сальникова Екатерина, победителями выставки творческих работ «Добро пожаловать в Литву!» стали Лихацая Александра, Синюк Богдан, Шевцова Виктория, Зайцев Максим, Коваль Мария. Дорондова Елизавета, учащаяся 10А класса, была отмечена дипломом за подготовку выставки иллюстраций к литовским народным сказкам. Дипломами за подготовку и участие в проведении Дней литовской культуры были удостоены музыкальные коллективы гимназии «Звуки музыки» и «Канцона», а также солисты ансамблей Наталия Белая и Анна Анисимова, вокальные номера в исполнении которых, прозвучавшие на литовском языке, стали украшением церемонии.
</p>
