<p style="text-align: justify;">
 <b>Зотов Иван Георгиевич</b><br>
 (с 1960 по 1961 гг.)&nbsp; </span>
</p>
<p style="text-align: justify;">
 Назначен директором с 1 сентября 1960 года. Ранее работал на рыболовецких судах. Морская профессия для него оказалась более привлекательной, и через год он вновь вернулся в море. Но, &nbsp;даже находясь в дальних плаваниях, не забывал о школе, присылал коллективу поздравительные телеграммы.
</p>
