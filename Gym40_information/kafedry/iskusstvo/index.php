<p style="text-align: justify;">
	 Кафедра объединяет творческих, инициативных педагогов, ориентированных на занятия инновационной, научно-исследовательской, экспериментальной деятельностью.
</p>
<h5>Цель кафедры:</h5>
<p>
	 научно-методическое обеспечение развития образовательных процессов по учебным предметам ИЗО, музыка, технология.
</p>
<h5>Задачи кафедры:</h5>
<p>
	 • строить свою работу в соответствии со стратегическими документами гимназии;<br>
	 • разработка и экспертиза концепций новых учебных курсов (как обязательных, так и дополнительных), соответствующих учебных программ, образовательных технологий и методик;<br>
	 • обсуждение, рецензирование различных материалов, подготовленных на кафедре;<br>
	 • анализ хода и результатов инновационной деятельности;<br>
	 • прогнозирование изменений потребностей в научно-методическом обеспечении учебной работы по своему профилю;<br>
	 • проведение проблемного анализа результатов нововведений, непосредственно инициируемых кафедрой;<br>
	 • организация исследовательской работы;<br>
	 • разработка и коррекция стратегических документов гимназии в пределах своей компетенции;<br>
	 • внесение предложений по улучшению научно-методической работы;<br>
	 • организация различных творческих конкурсов, и пр.;
</p>
<p style="text-align: justify;">
	 Кафедра согласовывает свою деятельность с научно-методическим советом гимназии, заместителем директора по научно-методической работе, подотчетна педагогическому совету гимназии. Кафедра несет ответственность за качественную и своевременную реализацию принятых ею решений, осуществляет контроль выполняемых программ и мероприятий.
</p>
