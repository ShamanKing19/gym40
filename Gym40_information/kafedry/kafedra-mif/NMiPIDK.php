<h4 align="center"><b>Физико-технический центр «Клен» - ключевое звено кафедры МИФ</b></h4>
<p align="center">
 <img width="379" src="/Gym40_information/kafedry/kafedra-mif/documents/Рисунок 8 Клен.png" height="250" align="right">
</p>
<p style="text-align: justify;">
	 Развитие творческой, креативной личности всегда было в поле внимания кафедры МИФ. Данная проблема нашла отражение в новых образовательных стандартах: предусматривается вариативность образовательных программ, внедрение различных форм творческих занятий дляучащихся. ФТЦ служит идее развития креативности учащихся.
</p>
<p style="text-align: justify;">
	 ФТЦ гимназии является площадкой для предметных и межпредметных проектов по таким дисциплинам как: физика, информатика, биология, химия, иностранные языки, география, технология. На базе центра осуществляется индивидуальная проектная и исследовательская деятельность, интегрированные проекты.
</p>
<p style="text-align: justify;">
	 ФТЦ руководствуется определенными правилами в реализации проектов: <br>
	 1. Если всячески помогать и одобрять труд ученика, стимулировать его творческие идеи, то именно они будут расти в нем и развиваться. <br>
	 2. Стараться ничему не учить школьника напрямую. Побуждать учиться самим. Тогда ученик, находясь рядом с преподавателем, будет всегда видеть, чувствовать и знать, как можно учиться. <br>
	 3. Не задавать детям вопросов, на которые мы знаем ответы. Искать истину вместе с ними. Ощущать радость совместного с учениками творчества и открытия.
</p>
<p style="text-align: justify;">
	 Креативные методы обучения на базе Физико-технического центра являются своеобразным ключом доступа к интуитивному опыту, к различным структурам памяти, подсознанию. Такой доступ обеспечивает оригинальные, креативные решения задач и проблем, делает процесс познания эффективным и позволяет учащемуся проявить свои скрытые творческие возможности.
</p>
