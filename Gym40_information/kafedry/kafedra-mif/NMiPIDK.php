<h4 align="center"><b>
Инновационная модель физико-математического образования</b> </h4>
<p style="text-align: justify;">
	 Для участия в региональном проекте создания опорной площадки по физико-математическому направлению гимназией № 40 первоначально были заявлены 7 и 8 классы физико-математического профиля (2013 год). В настоящее время деятельность по созданию инновационной модели физико-математического образования осуществляется комплексно в 5-11 классах. В 5-8 классах реализуется образовательная программа, разработанная в соответствии с ФГОС ООО.
</p>
<p style="text-align: justify;">
