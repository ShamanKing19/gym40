	 Управление гимназией осуществляется в соответствии с законодательством Российской Федерации с учетом особенностей, установленных Федеральным законом «Об образовании в Российской Федерации», ст. 26.
</p>
<p>
 <b>Структурные подразделения и филиалы отсутствуют.</b>
</p>
<p>
	 Управление гимназией осуществляется на основе сочетания принципов единоначалия и коллегиальности.
</p>
<p>
	 Единоличным исполнительным органом образовательной организации является директор, который осуществляет текущее руководство деятельностью образовательной организации.
</p>
<p>
	 В гимназии сформированы коллегиальные органы управления, к которым относятся:<br>
 <i class="fa fa-sign-out check" style="color: SteelBlue"></i> общее собрание трудового коллектива, <br>
 <i class="fa fa-sign-out check" style="color: SteelBlue"></i> педагогический совет, <br>
 <i class="fa fa-sign-out check" style="color: SteelBlue"></i> наблюдательный совет.<br>
</p>
<p>
	 Структура, порядок формирования, срок полномочий и компетенция органов управления гимназией, регламентируются уставом и другими локальными нормативными актами гимназии.
</p>
<p>
	 В целях учета мнения обучающихся, родителей (законных представителей) по вопросам управления гимназией созданы родительский совет гимназии, ученический совет, родительские комитеты классов.
