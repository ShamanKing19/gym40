<p>
 <b><i>Родительский совет</i></b> - орган самоуправления, который обеспечивает содействие администрации в совершенствовании условий для осуществления образовательного процесса, охраны жизни и здоровья обучающихся. Родительский совет обсуждает вопросы организации учебного процесса, экскурсионной работы, оказания помощи малообеспеченным семьям, контролирует качество питания детей. <br>
