</p>
<p>
 <b><i>Педагогический совет</i></b> - коллегиальный орган управления гимназии, рассматривающий основные вопросы образовательного процесса. <br>
 <a href="/Gym40_information/documents/lokalnye/Положение о педагогическом совете.pdf" target="_blank"><i class="fa fa-file-pdf-o fa-lg text-pdf" aria-hidden="true"></i> Положение о педагогическом совете</a>
</p>
<p>
 <b><i>Наблюдательный совет гимназии</i></b> - представительный орган управления гимназией, осуществляющий общее руководство и состоящий из представителей Учредителя (Комитет по образованию администрации Городского округа «Город Калининград») и работников гимназии. <br>
