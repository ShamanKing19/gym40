</p>
<p>
 <b><i>Общее собрание трудового коллектива</i></b> - высший орган управления гимназией, объединяющий всех работников учреждения. <br>
 <a href="/Gym40_information/documents/lokalnye/положение о стк.pdf" target="_blank"><i class="fa fa-file-pdf-o fa-lg text-pdf" aria-hidden="true"></i> Положение о Совете трудового коллектива</a>
