 <b>Организация преподавания комплексного учебного курса <br>
	 «Основы религиозных культур и светской этики»</b>
</p>
<p style="text-align:justify;">
	 Комплексный учебный курс «Основы религиозных культур и светской этики» введён&nbsp; в учебный план всех общеобразовательных учреждений Российской Федерации с 2012 года. В 2018-2019 учебном году комплексный учебный курс ОРКСЭ изучают 276 учащихся 4-х классов. Из них 128 – модуль «Основы православной культуры», 111 – «Основы светской этики», 37 – «Основы мировых религиозных культур».
</p>
<p style="text-align:justify;">
 <b>Цель курса</b> – формирование у обучающихся мотивации к осознанному нравственному поведению, основанному на знании и уважении культурных и религиозных традиций многонационального народа России, а также к диалогу с представителями других культур и мировоззрений.
</p>
<p style="text-align:justify;">
	 Информация о содержании и структуре курса:
</p>
