	</p>
 </li>
	<li><i class="fa-li fa fa-chevron-circle-right"></i>
	<p>
		 в рамках курса учащийся изучает ОДИН из модулей, для выбора предлагаются следующие модули: <br>
		 «Светская этика», <br>
		 «Основы мировых религиозных культур», <br>
		 «Основы православной культуры», <br>
		 «Основы буддийской культуры», <br>
