	<li>обсуждение предметных концепций (география, обществознание, искусство, технология, физическая культура);</li>
	<li>создание сетевых сообществ по учебным предметам и предметным областям.</li>
</ol>
<p style="text-align: justify;">
	 В ходе реализации проекта создана стажировочная сеть, объединившая гимназию с организациями высшего, дополнительного и общего образования. На базе этих образовательных организации происходит повышение квалификации педагогов Калининградской области и других субъектов РФ, а также внедряются инновации в работу информационно-библиотечных центров.
</p>
<p style="text-align: justify;">
	 Средства коммуникационной сети Интернет также служат реализации целей проекта: разработаны дистанционные курсы повышения квалификации для педагогов&nbsp;<a target="_blank" href="https://study.baltinform.ru/">https://study.baltinform.ru/</a>, часть обсуждения предметных концепций идёт на виртуальной площадке.
</p>
