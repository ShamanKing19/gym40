	 МАОУ гимназия № 40 имени Ю.А. Гагарина в 2016 г. является одной из стажировочных площадок региональной сети образовательных организаций, реализующих проект «Модернизация технологий и содержания обучения в соответствии с новым федеральным государственным образовательным стандартом посредством разработки концепций модернизации конкретных областей, поддержки региональных программ развития образования и поддержки сетевых методических объединений».
</p>
<p style="text-align: justify;">
	 Калининградская область стала регионом-победителем конкурсного отбора региональных программ развития образования для предоставления субсидий на поддержку реализации мероприятий ФЦПРО на 2016-2020 годы.
</p>
<p style="text-align: justify;">
 <b>Основные направления реализации проекта:</b>
</p>
<ol style="text-align: justify;">
