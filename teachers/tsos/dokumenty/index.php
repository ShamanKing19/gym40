 <b>Для ученика </b><br>
 <br>
 <i class="fa fa-check-circle check" aria-hidden="true"></i> Возможность подключиться к уроку или консультации, если нет возможности посетить очное занятие в школе;<br>
 <i class="fa fa-check-circle check" aria-hidden="true"></i> Решение общешкольных вопросов и беседы с классом в групповых чатах и с помощью видеозвонков;<br>
 <i class="fa fa-check-circle check" aria-hidden="true"></i> Трансляция онлайн-мероприятий, начиная от онлайн-уроков лучших педагогов России до мастер-классов обо всём, что может быть интересно школьникам;<br>
 <i class="fa fa-check-circle check" aria-hidden="true"></i> Простая регистрация;<br>
 <i class="fa fa-check-circle check" aria-hidden="true"></i> Привычный интерфейс - единый центр авторизации VK Connect;<br>
 <i class="fa fa-check-circle check" aria-hidden="true"></i> Всегда с собой - мобильное приложение для iOS и Android.
</p>
<p style="text-align: justify;">
 <b>Для учителя </b><br>
 <br>
 <i class="fa fa-check-circle check" aria-hidden="true"></i> Сообщества школ — закрытые группы, в которые можно приглашать учителей, родителей и учеников;<br>
 <i class="fa fa-check-circle check" aria-hidden="true"></i> Управление классами и назначение заданий, возможность загружать образовательный контент, делиться видео, статьями и документами со всеми участниками сообщества;<br>
 <i class="fa fa-check-circle check" aria-hidden="true"></i> Закрытые конференции до 100 участников;<br>
 <i class="fa fa-check-circle check" aria-hidden="true"></i> Возможность дистанционного подключения к уроку всего класса или отдельных учеников;<br>
 <i class="fa fa-check-circle check" aria-hidden="true"></i> Каждый из собеседников может включить демонстрацию экрана или виртуально поднять руку.
</p>
