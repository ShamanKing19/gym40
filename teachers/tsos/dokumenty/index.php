 <a target="_blank" href="http://neorusedu.ru/about">Цифровая образовательная среда (ЦОС)</a> – это приоритетный проект в сфере образования, который являет собой совокупность условий, созданных для реализации образовательных программ с применением электронного обучения, дистанционных образовательных технологий, с учетом функционирования электронной информационно-образовательной среды, включающей в себя электронные информационные ресурсы, электронные образовательные ресурсы, совокупность информационных ‎и телекоммуникационных технологий, соответствующих технологических средств, и обеспечивающих освоение обучающимися образовательных программ в полном объеме независимо от места нахождения обучающихся. Целью данного проекта является качественное и доступное онлайн-обучение граждан с помощью цифровых технологий. <br>
