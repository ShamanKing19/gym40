<p style="text-align: justify;">
	 Фестиваль наук в гимназии проводится ежегодно. В 2016 году организаторы мероприятия (учителя кафедры математики, информатики и физики) предложили участникам образовательного процесса провести с 1 по 8 декабря Фестиваль в новом формате. Планируется организовать несколько секций, в которых будут заслушаны доклады о научных достижениях в различных областях. В качестве докладчиков выступят все ученики профильных классов 7-й, 8-й и 10-й параллелей.
</p>
<p style="text-align: justify;">
	 Для привлечения внимания обучающихся к научным достижениям учителя профильных предметов подбирают темы докладов в соответствии с предметной, межпредметной и надпредметной проблематикой, в том числе в соответствии с тематикой исследований лауреатов Нобелевской премии. Обсуждается формат доклада учащихся: устный или стендовый. В каждой секции будет организована работа слушателей по оцениванию выступлений и определены кандидатуры докладчиков на итоговое мероприятие, которое мы называем гимназические «Нобелевские чтения».
</p>
<p style="text-align: justify;">
	 Классные руководители, учителя профильных предметов планируют проведение классных часов и фрагментов уроков, отвечающих цели мероприятия «Фестиваль наук. Нобелевские чтения». Будут использованы для беседы с учащимися видеозаписи популярных научных лекций из сети Интернет. В 10-е классы приглашены&nbsp; учёные БФУ им. И. Канта для выступления на классном часе. В период проведения мероприятия «Фестиваль наук. Нобелевские чтения» классные коллективы смогут поучаствовать в просмотре и обсуждении фильмов в рамках Дней научного кино ФАНК в Балтийском федеральном университете имени И. Канта. ФАНК второй год подряд проводит масштабный просветительский проект в российских вузах – Дни научного кино ФАНК. С 5 по 10 декабря проект проходит при поддержке Министерства образования и науки РФ.
</p>
