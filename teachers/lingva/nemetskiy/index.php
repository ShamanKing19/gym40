<p style="text-align: justify;">
	 Очередное достижение коллектива учащихся и педагогов гимназии: по итогам конкурса образовательных организаций Калининградской области гимназия вошла в число участников проекта «Немецкий – первый второй иностранный» в 2016/2017 учебном году. Стоит ли этому удивляться, ведь традиция изучения второго иностранного языка в гимназии уходит в далеко в прошлое.
</p>
<p style="text-align: justify;">
	 Первые группы формировались еще в конце 90-х годов прошлого столетия. Начиная с 2001 года, изучение второго иностранного языка становится обязательным для классов, обучавшихся по лицейской, чуть позже по гимназической программе. Учащиеся могли выбрать английский, немецкий, польский, литовский или французский язык в качестве второго иностранного. Не удивительно, что немецкий язык выбирался учащимися наиболее часто.
</p>
<p style="text-align: justify;">
	 На сегодняшний день второй иностранный язык обязателен для всех учащихся <a href="/teachers/lingva/nemetskiy/фото1.jpg" target="_blank"><img width="120" src="/teachers/lingva/nemetskiy/фото1.jpg" height="170" hspace="7" align="right"></a>5-6 классов и для всех классов лингвистического направления. Немецкий язык как второй иностранный изучают 225 учащихся с 5 по 10 класс. Основным учебником является УМК М.М. Аверина «Горизонты».
</p>
<p style="text-align: justify;">
	 Для участия в проекте «Немецкий – первый второй иностранный» был выбран 5 «В» класс – лингвистический, но в действительности все классы параллели пятых и шестых классов участвуют во всех урочных и внеурочных мероприятиях и нестандартных уроках. Впереди – олимпиада по немецкому языку для пятиклассников, квест «Удивительный мир немецкой культуры», урок-игра «Мой немецкий» и много других увлекательных событий для юных любителей немецкого языка.
</p>
