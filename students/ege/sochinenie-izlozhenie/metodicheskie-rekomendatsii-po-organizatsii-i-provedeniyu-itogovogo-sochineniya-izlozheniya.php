		<h6>Проведение итогового сочинения (изложения)</h6>
		<p>
			 Итоговое сочинение (изложение) проводится в образовательных организациях, реализующих образовательные программы среднего общего образования, и (или) в местах проведения итогового сочинения (изложения), определенных регионом.
		</p>
		<p>
			 Итоговое сочинение (изложение) начинается в 10.00 по местному времени.
		</p>
		<h6>Ознакомление с результатами и срок действия </h6>
		<p>
			 С результатами итогового сочинения (изложения) участники могут ознакомиться в образовательных организациях или в местах регистрации на участие в итоговом сочинении (изложении). По решению региона ознакомление участников с результатами итогового сочинения (изложения) может быть организовано в информационно-телекоммуникационной сети «Интернет» в соответствии с требованиями законодательства Российской Федерации в области защиты персональных данных.
		</p>
		<p>
			 Итоговое сочинение (изложение) как допуск к ГИА – бессрочно.
		</p>
		<p>
			 Итоговое сочинение в случае представления его при приеме на обучение по программам бакалавриата и программам специалитета действительно в течение четырех лет, следующих за годом написания такого сочинения. Выпускники прошлых лет могут участвовать в написании итогового сочинения, в том числе при наличии у них итогового сочинения прошлых лет.
		</p>
		<p>
			 Выпускники прошлых лет, изъявившие желание повторно участвовать в написании итогового сочинения, вправе предоставить в образовательные организации высшего образования итоговое сочинение только текущего года, при этом итоговое сочинение прошлого года аннулируется.
		</p>
