		<h6>Итоговое сочинение (изложение) проводится для обучающихся XI (XII) классов, в том числе для:</h6>
		<p>
 <i class="fa fa-sign-out check" style="color: RoyalBlue"></i> Иностранных граждан, лиц без гражданства, в том числе соотечественников за рубежом, беженцев и вынужденных переселенцев, освоивших образовательные программы среднего общего образования в очной, очно-заочной или заочной формах, а также для лиц, освоивших образовательные программы среднего общего образования в форме семейного образования или самообразования;<br>
 <i class="fa fa-sign-out check" style="color: RoyalBlue"></i> Лиц, обучавшихся по не имеющей государственной аккредитации образовательной программе среднего общего образования, а также обучающихся, получающих среднее общее образование в рамках освоения образовательных программ среднего профессионального образования, в том числе образовательных программ среднего профессионального образования, интегрированных с образовательными программами основного общего и среднего общего образования (в случае прохождения ГИА экстерном в организации, осуществляющей образовательную деятельность по имеющей государственную аккредитацию образовательной программе среднего общего образования с последующим получением аттестата о среднем общем образовании);<br>
 <i class="fa fa-sign-out check" style="color: RoyalBlue"></i> Обучающихся по образовательным программам среднего общего образования в специальных учебно-воспитательных учреждениях закрытого типа, а также в учреждениях, исполняющих наказание в виде лишения свободы;<br>
 <i class="fa fa-sign-out check" style="color: RoyalBlue"></i> Обучающихся с ограниченными возможностями здоровья, детей-инвалидов и инвалидов по образовательным программам среднего общего образования.<br>
