 <i class="fa fa-sign-out check" style="color: RoyalBlue"></i> Обучающиеся по образовательным программам среднего общего образования в специальных учебно-воспитательных учреждениях закрытого типа, а также в учреждениях, исполняющих наказание в виде лишения свободы;<br>
 <i class="fa fa-sign-out check" style="color: RoyalBlue"></i> Обучающиеся на дому, в образовательных организациях, в том числе санаторно-курортных, в которых проводятся необходимые лечебные, реабилитационные и оздоровительные мероприятия для нуждающихся в длительном лечении на основании заключения медицинской организации.<br>
