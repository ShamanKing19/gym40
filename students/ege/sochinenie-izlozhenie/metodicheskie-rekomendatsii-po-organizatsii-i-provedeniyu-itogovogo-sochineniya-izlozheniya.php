		<h6>Порядок подачи заявления на участие в итоговом сочинении (изложении)</h6>
		<p>
			 Для участия в итоговом сочинении (изложении) участники подают заявление и согласие на обработку персональных данных не позднее чем за две недели до начала проведения итогового сочинения (изложения).
		</p>
		<p>
			 Регистрация обучающихся для участия в итоговом сочинении (изложении) проводится на основании их заявлений в организациях, осуществляющих образовательную деятельность, в которых обучающиеся осваивают образовательные программы среднего общего образования.
		</p>
		<p>
			 Обучающиеся с ограниченными возможностями здоровья при подаче заявления на написание итогового сочинения (изложения) предъявляют копию рекомендаций психолого-медико-педагогической комиссии, а обучающиеся дети-инвалиды и инвалиды - оригинал или заверенную в установленном порядке копию справки, подтверждающей факт установления инвалидности, выданной федеральным государственным учреждением медико-социальной экспертизы.
		</p>
		<p>
			 Регистрация лиц для участия по их желанию в итоговом сочинении проводится в местах, определяемых регионом.
		</p>
		<p>
			 Регистрация лиц со справкой об обучении для участия по их желанию в итоговом сочинении проводится в организациях, осуществляющих образовательную деятельность, в которых указанные лица восстанавливаются на срок, необходимый для прохождения ГИА. При подаче заявления такие лица предъявляют справку об обучении по образцу, самостоятельно устанавливаемому организацией, осуществляющей образовательную деятельность.
		</p>
		<p>
			 Лица, участвующие в сочинении по желанию, самостоятельно выбирают дату участия в итоговом сочинении из числа установленных расписанием проведения итогового сочинения (изложения). Выбранную дату участия в итоговом сочинении такие лица указывают в заявлении.
		</p>
		<p>
			 Выпускники прошлых лет при подаче заявления на прохождение итогового сочинения предъявляют оригиналы документов об образовании. Оригинал иностранного документа об образовании предъявляется с заверенным в установленном порядке переводом с иностранного языка. Указанное заявление подается лично или их родителями (законными представителями) на основании документа, удостоверяющего их личность, или уполномоченными лицами на основании документа, удостоверяющего их личность, и оформленной в установленном порядке доверенности.
		</p>
