		<h6>Итоговое сочинение в целях использования его результатов при приеме в образовательные организации высшего образования по желанию также может проводиться для:</h6>
		<p>
 <i class="fa fa-sign-out check" style="color: RoyalBlue"></i> Лиц, освоивших образовательные программы среднего общего образования в предыдущие годы и имеющих документ об образовании, подтверждающий получение среднего общего образования (или образовательные программы среднего (полного) общего образования - для лиц, получивших документ об образовании, подтверждающий получение среднего (полного) общего образования (до 1 сентября 2013 года);<br>
 <i class="fa fa-sign-out check" style="color: RoyalBlue"></i> Граждан, имеющих среднее общее образование, полученное в иностранных образовательных организациях;<br>
 <i class="fa fa-sign-out check" style="color: RoyalBlue"></i> Лиц, обучающихся по образовательным программам среднего профессионального образования;<br>
 <i class="fa fa-sign-out check" style="color: RoyalBlue"></i> Лиц, получающих среднее общее образование в иностранных образовательных организациях;<br>
 <i class="fa fa-sign-out check" style="color: RoyalBlue"></i> Лиц, допущенных к ГИА в предыдущие годы, но не прошедших ГИА или получивших на ГИА неудовлетворительные результаты более чем по одному обязательному учебному предмету, либо получивших повторно неудовлетворительный результат по одному из этих предметов на ГИА в дополнительные сроки (далее - лица со справкой об обучении).<br>
