<h4 style="text-align: justify;">Ознакомление обучающихся с результатами ГИА и условиями повторного допуска к сдаче экзаменов в текущем учебном году</h4>
<p style="text-align: justify;">
	 После утверждения ГЭК результаты ГИА в течение одного рабочего дня передаются в ОО, а также ОМСУ для последующего ознакомления обучающихся с утвержденными результатами ГИА. Ознакомление обучающихся с полученными ими результатами ГИА по учебному предмету осуществляется в течение одного рабочего дня со дня их передачи в образовательные организации, а также ОМСУ. Указанный день считается официальным днем объявления результатов.
</p>
<p style="text-align: justify;">
	 По решению ОИВ ознакомление обучающихся осуществляется с использованием информационно-коммуникационных технологий в соответствии с требованиями законодательства Российской Федерации в области защиты персональных данных.
</p>
<p style="text-align: justify;">
	 По решению ГЭК повторно допускаются к сдаче экзаменов в дополнительные сроки (резервные сроки) в текущем учебном году по соответствующему учебному предмету следующие обучающиеся:<br>
	 - получившие на ГИА неудовлетворительный результат не более чем по двум учебным предметам;<br>
	 - не явившиеся на экзамены по уважительным причинам (болезнь или иные обстоятельства, подтвержденные документально);<br>
	 - не завершившие выполнение экзаменационной работы по уважительным причинам (болезнь или иные обстоятельства, подтвержденные документально);<br>
	 апелляция которых о нарушении установленного порядка проведения ГИА КК была удовлетворена;<br>
	 результаты которых были аннулированы ГЭК в случае выявления фактов нарушений установленного порядка проведения ГИА, совершенных лицами, указанными в пункте 37 Порядка, или иными (неустановленными) лицами.
</p>
